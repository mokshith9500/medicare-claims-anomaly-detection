// frontend for the claims integrity console

const riskColor = s =>
  s >= 0.85 ? 'var(--risk-hi)' : s >= 0.6 ? 'var(--risk-mid)' : 'var(--risk-lo)';

let activeRow = null;

async function loadQueue(){
  const res = await fetch('/api/providers');
  const rows = await res.json();
  const q = document.getElementById('queue');
  q.innerHTML = '';
  rows.forEach((r, i) => {
    const el = document.createElement('div');
    el.className = 'qrow';
    el.innerHTML = `
      <span class="qrank">${String(i+1).padStart(2,'0')}</span>
      <div>
        <div class="qid">${r.id}<span class="dot" style="background:${riskColor(r.score)}"></span></div>
        <div class="qsub">$${r.dollars.toLocaleString()}/claim · ${r.claims} claims</div>
      </div>
      <span class="qscore">${r.score.toFixed(3)}</span>`;
    el.onclick = () => { selectProvider(r.id); setActive(el); };
    q.appendChild(el);
  });
}

function setActive(el){
  if(activeRow) activeRow.classList.remove('active');
  el.classList.add('active'); activeRow = el;
}

async function selectProvider(id){
  const res = await fetch('/api/provider/' + id);
  const d = await res.json();
  document.getElementById('empty').hidden = true;
  document.getElementById('detail').hidden = false;

  document.getElementById('d-id').textContent = d.id;
  document.getElementById('d-score').textContent = d.score.toFixed(3);
  const fill = document.getElementById('d-meter');
  fill.style.width = (d.score*100) + '%';
  fill.style.background = riskColor(d.score);

  const chips = document.getElementById('d-chips');
  const truth = d.label === 'Yes'
    ? '<span class="chip truth-yes">ground truth: fraud (demo)</span>'
    : '<span class="chip truth-no">ground truth: clean (demo)</span>';
  chips.innerHTML = (d.flagged ? '<span class="chip flag">flagged for review</span>' : '') + truth;

  drawBars(d.deviations);

  // reset briefing
  document.getElementById('d-explain').textContent =
    'Generates a plain-language summary of the evidence above. The model only narrates the computed deviations; it does not decide.';
  const tag = document.getElementById('d-source'); tag.hidden = true;
  const btn = document.getElementById('explain-btn');
  btn.disabled = false; btn.onclick = () => getExplanation(id);
}

function drawBars(devs){
  const top = devs.slice(0, 8);
  const maxZ = Math.max(...top.map(d => Math.abs(d.z)), 3);
  const bars = document.getElementById('d-bars');
  bars.innerHTML = '';
  top.forEach(d => {
    const half = Math.min(Math.abs(d.z)/maxZ, 1) * 50;
    const pos = d.z >= 0;
    const color = Math.abs(d.z) >= 3 ? 'var(--risk-hi)'
                : Math.abs(d.z) >= 1.5 ? 'var(--risk-mid)' : 'var(--accent)';
    const row = document.createElement('div');
    row.className = 'bar-row';
    row.innerHTML = `
      <div class="bar-name" title="${d.feature}">${d.feature}</div>
      <div class="bar-track">
        <div class="bar-mid"></div>
        <div class="bar-fill" style="${pos?`left:50%;width:${half}%`:`right:50%;width:${half}%`};background:${color}"></div>
      </div>
      <div class="bar-z">${d.z>0?'+':''}${d.z.toFixed(1)}σ</div>`;
    bars.appendChild(row);
  });
}

async function getExplanation(id){
  const btn = document.getElementById('explain-btn');
  const out = document.getElementById('d-explain');
  btn.disabled = true; out.textContent = 'Generating…';
  try{
    const res = await fetch('/api/explain/' + id, {method:'POST'});
    const d = await res.json();
    out.textContent = d.text;
    const tag = document.getElementById('d-source');
    tag.textContent = d.source === 'llm' ? 'generated by llama 3.1 via groq' : 'template (no model key)';
    tag.hidden = false;
  }catch(e){ out.textContent = 'Could not generate explanation. Check the server log.'; }
  btn.disabled = false;
}

loadQueue();
